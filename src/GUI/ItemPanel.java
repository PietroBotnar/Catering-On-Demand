    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        //when add button is pressed the quantity increments by 1
        quant++;
        //new quantity is displayed
        quantity.setText(Integer.toString(quant));
        //ordered quantity is set in the item object
        item.setOrdered_quantity(quant);
        //change color background for selected items
        setBackground(Color.yellow);
    }//GEN-LAST:event_addButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
       //when remove button is pressed the quantity decrements by 1 if is not negative
        if (quant > 0) {
            quant--;
            //new quantity is displayed
            quantity.setText(Integer.toString(quant));
            //ordered quantity is set in the item object
            item.setOrdered_quantity(quant);
        }
        if (quant <= 0) {
            //change color background to default if items are not selected(ie quantity is zero)
            setBackground(new Color(240, 240, 240));
        }
        repaint();
    }//GEN-LAST:event_removeButtonActionPerformed
