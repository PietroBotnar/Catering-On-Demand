    double discount = 0;

    //panel form constructor
    //with a list of orders made by a customer
    public PaymentCheckoutPanel(MainFrame mf, ArrayList<Order> o, Customer c) {
        this.mf = mf;
        this.orders = o;
        this.customer = c;
        initComponents();
        PopulateYears();
        discountLabel.setVisible(false);

        //check if the customer has a vip status
        if (customer.getVip_status() != null) {
            //get discount details
            discountDetails = db.GetDiscount();
            //check if discount is active
            if (discountDetails.get(2).toString().equals("true")) {
                //calculate discount
                double tmp = Double.parseDouble(discountDetails.get(0).toString());
                if (tmp != 0) {
                    if (discountDetails.get(1).toString().equals("flexible")) {
                        discount = customer.getSpending() * (tmp / 100);
                    } else {
                        discount = tmp;
                    }
                }
                //display the discount
                String discountString = df.format(discount);
                discountLabel.setVisible(true);
                discountLabel.setText("-" + discountString + "% Discount");
                //add discount to orders
                for (Order or : orders) {
                    or.addDiscount(discount);
                }
            }
        }
        //check if orders have high priority and show the label accordingly
        for (Order or : orders) {
            if (or.isPriority()) {
                priorityLabel.setVisible(true);
            } else {
                priorityLabel.setVisible(false);
            }
            tot_amount += or.getTotal_price();
        }
        //display total amount
        totLabel.setText("Â£ " + df.format(tot_amount));
    }

