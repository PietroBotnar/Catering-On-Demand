    private void payButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payButtonActionPerformed
        //pay button action listener
        //checks if all fields are not empty
        //(fields are formatted to accept specific data format)
        if ((!address.getText().isEmpty()) && (!nameOnCard.getText().isEmpty()) && cvv.getValue() != null
                && cardNumber.getValue() != null) {
            mf.remove(this.getParent());
            //orders are added to the database
            for (Order o : orders) {
                db.InsertOrder(o);
            }
            //payment details are stored into the database
            String exp = exp_m.getSelectedItem().toString() + "/" + exp_y.getSelectedItem().toString();
            db.InsertPayment(nameOnCard.getText(), cardNumber.getText(), cvv.getText(), exp, address.getText());
            //customer spending is updated
            customer.addSpending(tot_amount);

            //confirmation message is displayed to the customer
            JOptionPane.showMessageDialog(null, "Payment Successful!");
            //menu panel is reset and added to frame
            mf.cd.menu.reset();
            mf.add(mf.cd.menu, BorderLayout.SOUTH);
            mf.pack();
            reset();
        } else {
            //if empty fields are present a message is displayed
            JOptionPane.showMessageDialog(null, "Please fill in all fields!");
        }

    }//GEN-LAST:event_payButtonActionPerformed

